#pragma once
#include <DxLib.h>
#include <memory>

/// <summary>
/// カメラの管理クラス
/// </summary>
class Camera
{
public:
	// コンストラクタ
	Camera();

	// デストラクタ
	virtual ~Camera();

	/// <summary>
	/// 更新
	/// </summary>
	/// <param name="playerPos">プレイヤーの位置座標</param>
	void Update(VECTOR playerPos);

	// 描画
	void Draw();

	// カメラの回転情報の取得
	float GetCameraYaw();	// Y軸

	float GetCameraFar() const;

	// カメラの位置の取得
	VECTOR GetPos();

	// カメラの注視点の取得
	VECTOR GetTarget();

private:
	// カメラの位置
	VECTOR cameraPos_;

	// カメラの注視点
	VECTOR cameraTarget_;

	// 視野角
	float perspective_;

	// 回転角度
	float cameraYaw_;	// Y軸
	float cameraPitch_;	// X軸
};